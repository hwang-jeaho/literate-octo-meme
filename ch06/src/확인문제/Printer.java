package 확인문제;

public class Printer {
	int num1;
	boolean what;
	double num2;
	String user;
	
	void println(int num1){
		this.num1=num1;
	System.out.println(num1);
	}
	void println(boolean what){
		this.what=what;
		System.out.println(what);
	}
	void println(double num2){
		this.num2=num2;
		System.out.println(num2);
	}
	void println(String user){
		this.user=user;
		System.out.println(user);
	}
	}

